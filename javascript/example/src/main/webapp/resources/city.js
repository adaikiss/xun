function cityLevel2(cityLevel1Name, cityLevel2Id, cityLevel2Name) {
	this.cityLevel1Name = cityLevel1Name;
	this.cityLevel2Id = cityLevel2Id;
	this.cityLevel2Name = cityLevel2Name;
	this.cityLevel3Array = new Array();
	this.addCity = function(cityId, cityName) {
    	this.cityLevel3Array = this.cityLevel3Array.concat({'cityId':cityId,'cityName':cityName});	
    };
	this.getCityLevel3Array = function () {
		return this.cityLevel3Array;
	};
}

	
function findCityLevel1(cityLevel1Id) {
	for ( var i = 0; i < cityLevel1Array.length; i++ ) {
		if ( cityLevel1Id == cityLevel1Array[i].cityLevel1Id ) {
			return cityLevel1Array[i];	
		}
	}
	return null;	
}

function findCityLevel2(cityLevel1Id, cityLevel2Id) {
	var oCityLevel1 = findCityLevel1(cityLevel1Id);
	var cityLevel2Array = oCityLevel1.getCityLevel2Array();
	for ( var i = 0; i < cityLevel2Array.length; i++ ) {
		if ( cityLevel2Id == cityLevel2Array[i].cityLevel2Id ) {
			return cityLevel2Array[i];	
		}
	}
	return null;
}

function findCityLevel3(cityLevel1Id, cityLevel2Id, cityLevel3Id) {
	var oCityLevel2 = findCityLevel2(cityLevel1Id, cityLevel2Id);
	var cityLevel3Array = oCityLevel2.getCityLevel3Array();
	for ( var i = 0; i < cityLevel3Array.length; i++ ) {
		if ( cityLevel3Id == cityLevel3Array[i].cityId ) {
			return cityLevel3Array[i];	
		}
	}
	return null;
}

/**
 * 根据城市编码查询全称
 * @param cityCode
 */
function findCityName(cityCode){
	var codes = cityCode.split('_');
	if(codes.length == 0){
		return ['全国'];
	}
	if(codes.length < 3){
		codes.push('BX');
		codes.push('BX');
	}
	var city2 = findCityLevel2(codes[0], codes[0] + '_' + codes[1]);
	if(!city2){
		var city1 = findCityLevel1(codes[0]);
		if(!city1){
			return ['全国'];
		}else{
			return [city1.cityLevel1Name];
		}
	}
	var cityLevel3Array = city2.getCityLevel3Array();
	var city3 = null;
	for ( var i = 0; i < cityLevel3Array.length; i++ ) {
		if ( cityCode == cityLevel3Array[i].cityId ) {
			city3 = cityLevel3Array[i];	
			break;
		}
	}
	var citys = [city2.cityLevel1Name];
	if(codes[1] != 'BX'){
		citys.push(city2.cityLevel2Name);
	}
	if(!city3){
		return citys;
	}
	if(codes[1] != 'BX' && codes[2] != 'BX'){
		citys.push(city3.cityName);
	}
	return citys;
}