package org.adaikiss.kay.trys;

public class AndTry {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		System.out.println("1 & -1 = " + (1 & -1));
		System.out.println("1 & 1 = " + (1 & 1));
		System.out.println("-1 & -1 = " + (-1 & -1));
		System.out.println("1 & 0 = " + (1 & 0));
		System.out.println("-1 & 0 = " + (-1 & 0));
		System.out.println(2<<3);
	}

}
